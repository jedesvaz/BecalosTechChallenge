let nota = Math.random()*100;
if (nota >= 90){
    console.log("La nota es de", nota, "Excelente")
} else if (nota >= 75 && nota <= 89){
    console.log("La nota es de ", nota, "Bien")
} else if (nota >= 60 && nota <= 74 ){
    console.log("La nota es de ", nota, "Suficiente")
} else {
    console.log("La nota es de ", nota, "No aprueba")
}
     
    
